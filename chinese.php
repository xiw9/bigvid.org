                    <h2>实验室简介</h2>
                    <hr>
                    <p>视频作为一种重要的媒体数据，占据着互联网上约80%的流量，人们的生活越来越离不开视频内容的智能分析与检索技术。在互联网视频分享与浏览的背后，这些技术可以大幅提升用户体验。在视频监控领域，智能分析系统可以帮助提升公共场所的管理水平、保卫生命财产安全。
                    </p>
                    <p>复旦大学计算机科学技术学院的视频大数据分析实验室（BigVid）隶属于上海市视频技术与系统工程研究中心，是学院媒体计算研究方向重点支持的研究团队。实验室主要关注视频大数据智能分析的算法和应用系统，研究内容与多个领域均有交叉，如多媒体、计算机视觉、机器学习、信息检索、人机交互等。实验室目前重点关注的研究问题包括：（1）基于深度学习的视频分类和内容识别；（2）大规模网络视频中的片段拷贝检测；（3）航拍视频分析；（4）物体与人脸识别；（5）视频大数据分析的数据集和通用测试平台构建。
                    </p> 
                    <p>近期的研究成果发表于顶尖的国际期刊和会议中，并受到广泛关注，具有一定国际影响力。通过与业界的领先企业合作，部分研究成果已转化为实际应用系统。此外，近期研发的系统在国家关键地点的关键任务中起到了重要作用。实验室近期受到国家自然科学基金委、科技部、教育部、上海市科委、华为公司、中兴公司等多个国家机构和企业资助。
